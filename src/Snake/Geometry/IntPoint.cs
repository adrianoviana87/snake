namespace Snake.Geometry
{
    public struct IntPoint
    {
        private int x;
        private int y;

        public IntPoint(int x, int y)
        {
            this.x = x;
            this.y = y;
        }

        public int X
        {
            get
            {
                return x;
            }
        }

        public int Y
        {
            get
            {
                return y;
            }
        }
    }
}